class FiCoCo_LlamaDecoderLayer(LlamaDecoderLayer):
    def __init__(self, config: LlamaConfig, layer_idx: int):
        super().__init__(config, layer_idx)
        self.hidden_size = config.hidden_size
        self.layer_idx = layer_idx

        # Define layers
        self.self_attn = FiCoCo_LlamaSdpaAttention(config=config, layer_idx=layer_idx)
        self.mlp = LlamaMLP(config)
        self.input_layernorm = LlamaRMSNorm(config.hidden_size, eps=config.rms_norm_eps)
        self.post_attention_layernorm = LlamaRMSNorm(config.hidden_size, eps=config.rms_norm_eps)

    def forward(
        self,
        hidden_states: torch.Tensor,
        attention_mask: Optional[torch.Tensor] = None,
        position_ids: Optional[torch.LongTensor] = None,
        past_key_value: Optional[Tuple[torch.Tensor]] = None,
        output_attentions: Optional[bool] = False,
        use_cache: Optional[bool] = False,
        v_token=None,
        flag=None,
        ficoco_flag=None,
        **kwargs,
    ) -> Tuple[torch.FloatTensor, Optional[Tuple[torch.FloatTensor, torch.FloatTensor]]]:

        # Deprecation warning for padding_mask
        if "padding_mask" in kwargs:
            warnings.warn(
                "Passing `padding_mask` is deprecated and will be removed in v4.37. "
                "Please use `attention_mask` instead."
            )

        # Pre-Attention normalization and residual connection
        residual = hidden_states
        hidden_states = self.input_layernorm(hidden_states)

        # Self-attention computation
        hidden_states, self_attn_weights, present_key_value, metric, att_score = self.self_attn(
            hidden_states=hidden_states,
            attention_mask=attention_mask,
            position_ids=position_ids,
            past_key_value=past_key_value,
            output_attentions=output_attentions,
            use_cache=use_cache,
            flag=flag,
            layer_idx=self.layer_idx,
            **kwargs,
        )
        hidden_states = residual + hidden_states

        # FiCoCo processing
        ficoco_flag = 'ficoco_l' if self.layer_idx > control_encoding_layer and flag else 'pass'

        if AT and ficoco_flag == 'ficoco_l':
            metric1 = metric[:, :v_token]
            tem = hidden_states
            hidden_states_pre = hidden_states[:, :v_token]
            hidden_states_post = hidden_states[:, v_token:]

            if hidden_states.shape[1] - rr > 0 and rr > 0:
                # Filtering and merging
                merge_indices, remain_indices, att_score_list = Filter(
                    tem, att_score, v_token, rr, 
                    _ficoco_info["class_token"], _ficoco_info["distill_token"]
                )
                target_indice, top_values = Correlate(
                    merge_indices, att_score_list, att_score, rr
                )
                hidden_states_pre, _ficoco_info["size"] = merge_ficoco_l(
                    Compress, hidden_states_pre, merge_indices, remain_indices, 
                    target_indice, top_values, rr, _ficoco_info["size"]
                )
                hidden_states = torch.cat([hidden_states_pre, hidden_states_post], dim=1)

        # Post-Attention processing
        residual = hidden_states
        hidden_states = self.post_attention_layernorm(hidden_states)
        hidden_states = self.mlp(hidden_states)
        hidden_states = residual + hidden_states

        # Output
        outputs = (hidden_states,)
        if output_attentions:
            outputs += (self_attn_weights,)
        if use_cache:
            outputs += (present_key_value,)

        return outputs

    
